function x_dot = fourode(t, x, m0, c0, k0, e1, e2, S0, tt, BVX)
            
    x_dot = zeros(4, 1);

    %x1(t)=x(1)
    x_dot(1) = x(2); %x1'(t)
    x_dot(2) = x(3); %x1''(t)
    x_dot(3) = x(4); %x1'''(t)
    exp1 =...
            m0^(-2)*((-3)*e1^2*k0^2*x(1).^5+2*c0*e2*k0*x(2).^3+(c0^2+(-2)*k0*m0)*...
            x(3)+12*c0^2*e2*x(2).^2*x(3)+15*c0^2*e2^2*x(2).^4*x(3)+6*e1*k0*x(1).^...
            2*(c0*e2*x(2).^3+(-1)*m0*x(3))+(-1)*k0*x(1)*(k0+6*e1*m0*x(2).^2+(-6)*...
            c0*e2*x(2)*x(3))+2*e1*k0*x(1).^3*((-2)*k0+3*c0*e2*x(2)*x(3)));
    x_dot(4) = exp1; %x1''''(t)
        
end